local function shuffleAndCopy(t, dest, count)
    local size = #t
    for i = size, 2, -1 do
        local j = math.random(i)
        t[i], t[j] = t[j], t[i]
    end
    for i = 1, math.min(count, size) do
        dest[i] = t[i]
		dest[i] = {t[i][1], t[i][2], t[i][3], stuff[math.random(1,5)]}
    end
end

